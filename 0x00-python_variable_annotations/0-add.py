#!/usr/bin/env python3

'''
    Basic annotations for variables.
'''

def plus(num1: float, num2: float) -> float:
    """
        Adds two numbers together.

        Args:
            num1 (float): The first number.
            num2 (float): The second number.

        Returns:
            float: The sum of num1 and num2.

        Example:
            >>> plus(3.5, 2.5)
            6.0
    """
    return num1 + num2