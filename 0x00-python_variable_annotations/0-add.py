#!/usr/bin/env python3

'''
    Basic annotations for variables.
'''

def add(a: float, b: float) -> float:
    """
        Adds two numbers together.

        Args:
            a (float): The first number.
            b (float): The second number.

        Returns:
            float: The sum of a and b.

        Example:
            >>> plus(3.5, 2.5)
            6.0
    """
    return a + b